package csiebug.web.html.chart.xmlSwfBean;

public class XmlSwfChartTooltip {
	private String font;
	private Boolean bold;
	private Integer size;
	private String color;
	private Integer alpha;
	private String backgroundColor;
	private Integer backgroundAlpha;
	private Integer duration;
	private String shadow;
	private String bevel;
	private String glow;
	private String blur;
	
	public void setFont(String chartTooltipFont) {
		this.font = chartTooltipFont;
	}
	public String getFont() {
		return font;
	}
	public void setBold(Boolean chartTooltipBold) {
		this.bold = chartTooltipBold;
	}
	public Boolean getBold() {
		return bold;
	}
	public void setSize(Integer chartTooltipSize) {
		this.size = chartTooltipSize;
	}
	public Integer getSize() {
		return size;
	}
	public void setColor(String chartTooltipColor) {
		this.color = chartTooltipColor;
	}
	public String getColor() {
		return color;
	}
	public void setAlpha(Integer chartTooltipAlpha) {
		this.alpha = chartTooltipAlpha;
	}
	public Integer getAlpha() {
		return alpha;
	}
	public void setBackgroundColor(
			String chartTooltipBackgroundColor) {
		this.backgroundColor = chartTooltipBackgroundColor;
	}
	public String getBackgroundColor() {
		return backgroundColor;
	}
	public void setBackgroundAlpha(
			Integer chartTooltipBackgroundAlpha) {
		this.backgroundAlpha = chartTooltipBackgroundAlpha;
	}
	public Integer getBackgroundAlpha() {
		return backgroundAlpha;
	}
	public void setDuration(Integer chartTooltipDuration) {
		this.duration = chartTooltipDuration;
	}
	public Integer getDuration() {
		return duration;
	}
	public void setShadow(String chartTooltipShadow) {
		this.shadow = chartTooltipShadow;
	}
	public String getShadow() {
		return shadow;
	}
	public void setBevel(String chartTooltipBevel) {
		this.bevel = chartTooltipBevel;
	}
	public String getBevel() {
		return bevel;
	}
	public void setGlow(String chartTooltipGlow) {
		this.glow = chartTooltipGlow;
	}
	public String getGlow() {
		return glow;
	}
	public void setBlur(String chartTooltipBlur) {
		this.blur = chartTooltipBlur;
	}
	public String getBlur() {
		return blur;
	}
}
