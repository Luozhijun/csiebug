public class JQPlotAxes {
	private JQPlotAxis xaxis;
	private JQPlotAxis yaxis;
	
	public void setXaxis(JQPlotAxis xaxis) {
		this.xaxis = xaxis;
	}
	public JQPlotAxis getXaxis() {
		return xaxis;
	}
	public void setYaxis(JQPlotAxis yaxis) {
		this.yaxis = yaxis;
	}
	public JQPlotAxis getYaxis() {
		return yaxis;
	}
}
